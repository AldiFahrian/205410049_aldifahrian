    <h1>Teknologi IT Ini Akan Mempengaruhi Dunia Pendidikan Di Masa Depan</h1>
    <p>
        Teknologi IT saat ini berkembang begitu pesatnya, hingga kita semua tidak bias hidup tanpa menggunakan teknologi. Meskipun perkembangan teknologi tersebut banyak memberikan dampak negatif di dunia sosial, namun tidak sedikit pula penerapan teknologi tersebut yang berbuah sesuatu yang sangat positif bagi manusia.

Salah satu bidang yang telah berevolusi berkat teknologi dengan pesat adalah dunia pendidikan. Ucapkan selamat tinggal pada cara mendidik yang konvensional dan kaku, karena beberapa penerapan teknologi informasi ini akan mengubah wajah pendidikan di Indonesia.
    </p>
    <p>
        Menurut Fernando Uffe, seorang pakar pendidikan di Extramarks Indonesia, setidaknya ada 9 teknologi informasi yang akan mempengaruhi model pendidikan kita saat ini, yaitu:
    </p>
    <p>
        Video Based Learning :
    </p>
    <p>
        Jika selama lebih dari satu dekade, pembelajaran di sekolah didominasi dengan media ajar tulisan dan presentasi power point, maka kedepannya pembelajaran berbasis video akan semakin digemari. Hal tersebut dikarenakan model pola pikir anak di zaman sekarang yang cepat bosan apabila hanya disuguhkan tulisan atau gambar yang tidak bergerak. Maka sebuah video interaktif adalah jawabannya. Namun bukan rekaman guru yang mengajar, akan tetapi visualisasi dari materi atau konten mata pelajaran tersebut di dunia nyata.
    </p>
    <p>
        Gamification : 
    </p>
    <p>
        Perkembangan game online gratis yang sangat pesat dibarengi dengan harga paket data yang murah sehingga mampu dibeli oleh setiap anak disegala umur, membuat mereka cenderung kecanduan game dan kurang memperhatikan pelajaran. Karena itu metode gamification atau membawa sebuah permainan digital ke dalam mata pelajaran sangat mungkin dilakukan. Hal tersebut akan membuat siswa dapat menikmati pengalaman pembelajaran yang dibalut dalam sebuah aplikasi game yang menarik, diharapkan dengan gamification para siswa mampu secara terbuka  terlibat ke dalam aktifitas dan membuka diri terhadap kemungkinan belajar.
    </p>
    <p>
        Big Data : 
    </p>
    <p>
        Ucapkan selamat tinggal terhadap formulir kertas yang kuno, sekarang Big data dapat menampung segala macam data terkait sekolah dan pembelajaran secara digital, termasuk data siswa, guru, orang tua wali, sarana prasarana dan lain sebagainya. Data tersebut bisa terupdate dengan mudah dan dapat menganalisa data dengan baik sehingga mampu untuk memberikan solusi untuk proses pembelajaran siswa secara lebih efektif.
    </p>
    <p>
        Cloud Computing : 
    </p>
    <p>
        Jikalau di masa lampau semua data sekolah ditempatkan di lemari atau di gudang sekolah, maka sekarang semua data tersebut dapat disimpan secara online sehingga dapat menghemat tempat dan meminimalisir kerusakan data akibat tergerus waktu. Data yang diinginkan atau dibutuhkan dapat diambil dalam waktu yang sangat singkat dan tepat sasaran.
    </p>
    <p>
        Internet Of Things : 
    </p>
    <p>
        Dengan Internet of Things akan menghubungkan setiap device yang ada di sekolah, semua device siswa, guru dan penilik sekolah akan dapat terkoneksi secara bersama. Seluruh device yang meliputi keamanan, absensi, monitoring dan berbagai peralatan lainnya di sekolah akan dapat terhubung dan terkontrol dari satu ruang pusat.
    </p>
    <p>
        Cyber Security :
    </p>
    <p>
        Dengan adanya digitalisasi data di seluruh lingkungan sekolah. Kemanan data pribadi siswa, guru dan sekolah menjadi penting untuk diperhatikan. Karena itu dibutuhkan pengamanan digital yang canggih dilingkungan sekolah agar dapat menghondari penggunaan data yang tidak pada tempatnya oleh pihak yang tidak bertanggung jawab.
    </p>
